#include "DeckLinkAPI.h"
#include "PlaybackHelper.h"

IDeckLink *getFirstDeckLinkCard()
{
	IDeckLink *deckLink = NULL;
	IDeckLinkIterator *deckLinkIter = CreateDeckLinkIteratorInstance();
	
	if (deckLinkIter)
	{
		// get the first decklink card
		if (deckLinkIter->Next(&deckLink) != S_OK)
		{
			printf("Could not detect a DeckLink card\n");
		}
		
		deckLinkIter->Release();
	}
	else
	{
		printf("Could not enumerate DeckLink cards\n");
	}
	
	return deckLink;
}

int main (int argc, char * const argv[])
{
	PlaybackHelper*	helper = NULL;
	IDeckLink*		deckLink = NULL;	
	
	// Use the first decklink card
	deckLink = getFirstDeckLinkCard();	
	if (! deckLink)
		goto bail;

	// Create playback helper
	helper = new PlaybackHelper(deckLink);
	if (! helper->init())
		goto bail;

	// start playback
	if (helper->startPlayback())
	{
		printf("Press enter to exit\n");
		getchar();
		helper->stopPlayback();
	}
	
	
bail:
		
	if (deckLink)
		deckLink->Release();
	
	if(helper)
		helper->Release();
	
    return 0;
}
